#include "../../includes/minishell.h"

char	onlyquote(char *s)
{
	size_t	i;

	i = -1;
	while (*(s + ++i))
	{
		if (*(s + i) != '\'' && *(s + i) != '"')
			return (0);
	}
	return (1);
}
